//adição - subtração - multiplicação - divisão - modulo

//adição

function adicao(a, b) {
  return 'Resultado da adição = ' + (a + b);
}

console.log(adicao(5, 10));

//subtração

function subtracao(a, b) {
  return 'Resultado da subtração = ' + (a - b);
}

console.log(subtracao(5, 10));

//multiplicação

function multiplicacao(a, b) {
  return 'Resultado da multiplicação = ' + a * b;
}

console.log(multiplicacao(5, 10));

//divisão

function divisao(a, b) {
  return 'Resultado da divisão = ' + a / b;
}

console.log(divisao(5, 10));

//modulo

function modulo(a, b) {
  return 'Resultado do módulo = ' + (a % b);
}

console.log(modulo(10, 2));
